//1: Khai báo biến và in giá trị ra màn hình
// Yêu cầu : Định nghĩa các biến kiểu 'int' , 'float', 'char'.In giá trị chúng ra màn hình.

#include <stdio.h>

int main(){
    int a = 5;
    float b = 5.5;
    char c = 'A';

    printf("Gia tri cua int a la: %d\n", a);
    printf("Gia tri cua float b la: %f\n", b);
    printf("Gia tri cua char c la: %c\n", c);
    return 0;
}

